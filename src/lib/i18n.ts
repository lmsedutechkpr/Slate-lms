function getT(lang: 'en' | 'ta') {
  return (key: string, variables: { [key: string]: any } = {}) => {
    const keys = key.split('.');
    let value: any = translations[lang];

    for (const k of keys) {
      if (value && typeof value === 'object' && k in value) {
        value = value[k];
      } else {
        // Fallback to English if key missing in Tamil
        let fallback: any = translations['en'];
        for (const fk of keys) {
          if (fallback && typeof fallback === 'object' && fk in fallback) {
            fallback = fallback[fk];
          } else {
            return key;
          }
        }
        value = fallback;
        break;
      }
    }

    if (typeof value === 'string') {
      let result = value;
      Object.entries(variables).forEach(([vKey, vValue]) => {
        result = result.replace(`{${vKey}}`, String(vValue));
      });
      return result;
    }

    return key;
  };
}

export function useTranslation(lang: 'en' | 'ta' = 'en') {
  return { t: getT(lang), lang };
}

export function getTranslation(lang: 'en' | 'ta' = 'en') {
  return { t: getT(lang), lang };
}
